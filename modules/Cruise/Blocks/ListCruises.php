                    'label'     => __('Number Item')
                ],
                [
                    'id'            => 'style',
                    'type'          => 'select',
                    'label'         => __('Style'),
                    'values'        => [
                        [
                            'id'   => 'normal',
                            'name' => __("Normal")
                        ],
                        [
                            'id'   => 'carousel',
                            'name' => __("Slider Carousel")
                        ]
                    ],
                    "selectOptions"=> [
                        'hideNoneSelectedText' => "true"
                    ]
                ],
                [
                    'id'      => 'category_id',
                    'type'    => 'select2',
                    'label'   => __('Filter by Category'),
                    'select2' => [
                        'ajax'  => [
                            'url'      => url('/admin/module/cruise/category/getForSelect2'),
                            'dataType' => 'json'
                        ],
                        'width' => '100%',
                        'allowClear' => 'true',
                        'placeholder' => __('-- Select --')
                    ],
                    'pre_selected'=>url('/admin/module/cruise/category/getForSelect2?pre_selected=1')
                ],
                [
                    'id'      => 'location_id',
