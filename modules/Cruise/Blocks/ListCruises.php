    public function getName()
    {
        return __('Cruise: List Items');
    }

    public function content($model = [])
    {
        $model_Cruise = Cruise::select("bravo_cruises.*");
        if(empty($model['order'])) $model['order'] = "id";
        if(empty($model['order_by'])) $model['order_by'] = "desc";
        if(empty($model['number'])) $model['number'] = 5;
        if (!empty($model['location_id'])) {
            $location = Location::where('id', $model['location_id'])->where("status","publish")->first();
            if(!empty($location)){
                $model_Cruise->join('bravo_locations', function ($join) use ($location) {
                    $join->on('bravo_locations.id', '=', 'bravo_cruises.location_id')
                        ->where('bravo_locations._lft', '>=', $location->_lft)
                        ->where('bravo_locations._rgt', '<=', $location->_rgt);
                });
            }
        }
        if (!empty($model['category_id'])) {
            $category_ids = [$model['category_id']];
            $list_cat = CruiseCategory::whereIn('id', $category_ids)->where("status","publish")->get();
            if(!empty($list_cat)){
                $where_left_right = [];
                foreach ($list_cat as $cat){
                    $where_left_right[] = " ( bravo_cruise_category._lft >= {$cat->_lft} AND bravo_cruise_category._rgt <= {$cat->_rgt} ) ";
                }
                $sql_where_join = " ( ".implode("OR" , $where_left_right)." )  ";
                $model_Cruise
                    ->join('bravo_cruise_category', function ($join) use($sql_where_join) {
                        $join->on('bravo_cruise_category.id', '=', 'bravo_cruises.category_id')
                            ->WhereRaw($sql_where_join);
                    });
            }
        }
        if(!empty($model['is_featured']))
        {
            $model_Cruise->where('is_featured',1);
        }
        $model_Cruise->orderBy("bravo_cruises.".$model['order'], $model['order_by']);
        $model_Cruise->where("bravo_cruises.status", "publish");
        $model_Cruise->with('location');
        $model_Cruise->groupBy("bravo_cruises.id");
        $list = $model_Cruise->limit($model['number'])->get();
        $data = [
            'rows'       => $list,
            'style_list' => $model['style'],
            'title'      => $model['title'],
        ];
        return view('Cruise::frontend.blocks.list-cruise.index', $data);
    }
}
